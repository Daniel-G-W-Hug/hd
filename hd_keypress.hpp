#ifndef HD_KEYPRESS_HPP
#define HD_KEYPRESS_HPP

#include <iostream>

namespace hd
{

////////////////////////////////////////////////////////////////////////////////
// Interface
////////////////////////////////////////////////////////////////////////////////

void cmdl_wait_for_enter();

////////////////////////////////////////////////////////////////////////////////
// Implementation
////////////////////////////////////////////////////////////////////////////////

void cmdl_wait_for_enter()
{
    do
    {
        std::cout << '\n'
                  << "Press ENTER to continue...";
    } while (std::cin.get() != '\n');
}

} // namespace hd

#endif // HD_KEYPRESS_HPP